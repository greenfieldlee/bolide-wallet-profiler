CREATE TABLE "user" (
  "id" SERIAL PRIMARY KEY,
  "walletAddress" VARCHAR(255) UNIQUE NOT NULL,
  "createdAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "updatedAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE "position" (
  "id" SERIAL PRIMARY KEY,
  "protocol" VARCHAR(255) NOT NULL,
  "positionName" VARCHAR(255) NOT NULL,
  "depositedAmount" DECIMAL NOT NULL,
  "earning" DECIMAL NOT NULL,
  "updatedTime" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "userId" INTEGER NOT NULL,
  "createdAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "updatedAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY ("userId") REFERENCES "user"("id")
);

CREATE TABLE "alternative" (
  "id" SERIAL PRIMARY KEY,
  "protocol" VARCHAR(255) NOT NULL,
  "positionName" VARCHAR(255) NOT NULL,
  "depositedAmount" DECIMAL NOT NULL,
  "earning" DECIMAL NOT NULL,
  "updatedTime" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "userId" INTEGER NOT NULL,
  "createdAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "updatedAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY ("userId") REFERENCES "user"("id")
);

CREATE TABLE "protocol" (
  "id" SERIAL PRIMARY KEY,
  "title" VARCHAR(255) NOT NULL,
  "description" TEXT NOT NULL,
  "TVL" DECIMAL NOT NULL,
  "chainId" INTEGER NOT NULL,
  "startDate" DATE NOT NULL,
  "numberOfAudits" INTEGER NOT NULL,
  "numberOfParticipants" INTEGER NOT NULL,
  "poolTVL" DECIMAL NOT NULL,
  "createdAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "updatedAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);